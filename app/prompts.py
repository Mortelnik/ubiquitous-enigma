def get_prompt(prompt_key: str) -> dict:
    """Возвращает описание промпта или бросает понятную ошибку."""
    if prompt_key not in PROMPTS:
        valid = ", ".join(PROMPTS.keys())
        raise KeyError(f"Неизвестный промпт '{prompt_key}'. Доступны: {valid}")
    return PROMPTS[prompt_key]


def render_prompt(prompt_key: str, transcript: str, current_summary: str = "") -> str:
    """Безопасно подставляет данные в шаблон промпта.

    Для real-time промптов (needs_summary=True) подставляется также
    ``current_summary``; для остальных он игнорируется. Это предотвращает
    падение ``str.format`` из-за отсутствующего ключа ``{current_summary}``.
    """
    info = get_prompt(prompt_key)
    if info.get("needs_summary"):
        return info["text"].format(
            transcript=transcript, current_summary=current_summary or "(пусто)"
        )
    return info["text"].format(transcript=transcript)
